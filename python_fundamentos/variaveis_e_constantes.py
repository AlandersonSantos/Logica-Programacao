#VARIÁVEIS E CONSTANTES

#VARIÁVEIS:

#O que seria Variável: São valores que podem receber alterações no decorrer da exercução do programa.

#Variáveis são conhecidas também como "MUTÁVEIS" - é aquele que pode ter seu conteúdo alterado.

#Exemplo:

name = "Alan"
age = 22

print(name, age) #retorna Alan 22.

name = "Lianna"
age = 45
print(name, age) #retorna Lianna 45.

#Mutável → um caderno onde você pode apagar e escrever na mesma página.


#CONSTANTES:

#O que seria constantes: São valores fixo, que não recebe outros valores.

#Constantes são conhecidas também como "IMUTÁVEIS" - objeto imutável não pode ser alterado.

#OBS: O python não oferece um comando para criar CONSTANTES, para ficar explicito que é uma, escrevemos ela em MAIUSCULAS.

#Exemplo:

PI = 3.14 # Valor de pi nunca vai ser alterado, será um valor fixo.

Estados = ["AL", "RJ", "MG"] #Estado do Brasil, criamos uma constante para amazernar.

#Imutável → um livro impresso: se mudar algo, você tem que imprimir um novo.